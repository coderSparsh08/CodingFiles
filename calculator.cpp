#include<iostream>
using namespace std;
int main(){
    float num1, num2;
    cout<<"Enter the numbers in order so that difference & quotient can be calculated correctly"<<endl;
    cout<<"Enter num1 : "<<endl;
    cin>>num1;
    cout<<"Enter num2 : "<<endl;
    cin>>num2;
    cout<<"Sum : "<<(num1+num2)<<endl;
    cout<<"Difference : "<<(num1-num2)<<endl;
    cout<<"Quotient : "<<(num1/num2)<<endl;
    cout<<"Product : "<<(num1*num2)<<endl;
    return 0;
}